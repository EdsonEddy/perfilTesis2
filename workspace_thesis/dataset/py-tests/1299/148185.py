c = int(input())
for q in range(c):
    acu = 0
    t = int(input())
    for w in range(t):
        a = input().split()
        break
    ta = list(a)
    for y in range(t):
        b = input().split()
        break
    tb = list(b)
    for i in range(t):
        v = int(ta[i]) * int(tb[i])
        acu = acu + v
    print(acu)
